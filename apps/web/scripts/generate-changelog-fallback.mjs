// Snapshots packages/cli/CHANGELOG.md into lib/changelog-fallback.ts at build
// time. The live changelog page fetches from GitHub at runtime; this bundled
// copy ships inside the deploy and is used as an offline fallback so the page
// never renders empty when GitHub is unreachable.
//
// Runs from predev/prebuild. If the source can't be read (e.g. an unusual
// checkout), it leaves the existing committed snapshot untouched rather than
// clobbering it with an empty file.

import { readFileSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const source = join(here, "../../../packages/cli/CHANGELOG.md")
const output = join(here, "../lib/changelog-fallback.ts")

let content
try {
  content = readFileSync(source, "utf-8")
} catch (err) {
  console.warn(
    `[changelog] could not read ${source}; keeping existing fallback (${err.message})`
  )
  process.exit(0)
}

const file = `// AUTO-GENERATED by scripts/generate-changelog-fallback.mjs — do not edit.
// Build-time snapshot of packages/cli/CHANGELOG.md, used as an offline fallback
// when the live GitHub fetch is unavailable so the changelog never renders empty.
export const CHANGELOG_FALLBACK = ${JSON.stringify(content)}
`

writeFileSync(output, file)
console.log("[changelog] wrote lib/changelog-fallback.ts")
